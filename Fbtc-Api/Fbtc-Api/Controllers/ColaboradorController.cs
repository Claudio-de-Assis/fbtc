        // [Authorize]
        [Route("FindByFilters/{nome},{perfilId:int},{ativo}")]
        [HttpGet]
        public Task<HttpResponseMessage> FindByFilters(string nome, int perfilId,
            string ativo)
        {
            HttpResponseMessage response = new HttpResponseMessage();
            var tsc = new TaskCompletionSource<HttpResponseMessage>();

            try
            {
                bool? _ativo = null;

                if (ativo != null) {
                    if (ativo.Equals("true"))
                        _ativo = true;

                    if (ativo.Equals("false"))
                        _ativo = false;
                }

                var resultado = _colaboradorApplication.FindByFilters(nome, perfilId,
                    _ativo);

                response = Request.CreateResponse(HttpStatusCode.OK, resultado);

                tsc.SetResult(response);

                return tsc.Task;
            }
            catch (Exception ex)
            {
                if (ex.GetType().Name == "InvalidOperationException" || ex.Source == "prmToolkit.Validation")
                {
                    response = Request.CreateResponse(HttpStatusCode.NotFound);
                    response.ReasonPhrase = ex.Message;
                }
                else
                {
                    response = Request.CreateResponse(HttpStatusCode.BadRequest, ex.Message);
                }
                tsc.SetResult(response);

                return tsc.Task;
            }
        }

        // [Authorize]
        [Route("Colaborador")]
        [HttpPost]
        public Task<HttpResponseMessage> Post(Colaborador colaborador)
        {
            HttpResponseMessage response = new HttpResponseMessage();
            var tsc = new TaskCompletionSource<HttpResponseMessage>();
            string resultado = "false";

            try
            {
                if (colaborador == null) throw new InvalidOperationException("O objeto 'Colaborador' está nulo!");

                resultado = _colaboradorApplication.Save(colaborador);

                response = Request.CreateResponse(HttpStatusCode.OK, resultado);
                response.ReasonPhrase = resultado;

                tsc.SetResult(response);

                return tsc.Task;
            }
            catch (Exception ex)
            {
                if (ex.GetType().Name == "InvalidOperationException" || ex.Source == "prmToolkit.Validation")
